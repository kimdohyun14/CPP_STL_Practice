#include <iostream>
#include <string>
using namespace std;

int main()
{
	string s("Hello!");
	char buf[100];

	// 주의점: copy()는 끝에 '\0'문자를 포함하지 않는다.
	s.copy(buf, s.length());
	buf[s.length()] = '\0';
	cout << "전체 문자열[copy(buf,n)]: " << buf << endl;
	s.copy(buf, 4, 2); // s.copy(buf,n,off) s의 문자열을 buf로 off 위치부터 n개 복사
	buf[4] = '\0';
	cout << "부분 문자열[copy(buf, n, off)]: " << buf << endl;

	return 0;
}